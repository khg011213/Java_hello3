//이름 짓는버 낙타 타법 뱀 타법
public class Pie {

	public static void main(String[] args) {
		double pie = 10;
		double sq = pie*pie*3.14;
		System.out.println("반지름 : " + pie + " 넓이 : " + sq);
	
		final double PI = 3.14;
		double radius = 10;
		double area = radius*radius*PI;
		System.out.println("반지름 : " + radius + " 넓이 : " + area);
	}
}

